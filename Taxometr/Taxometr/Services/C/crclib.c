/*******************************************************************************
* Function Name  : Crc16
* Description    : –асчитывает контрольную сумму CRC16 CCITT табличным способом
*                : (ниже расчет нетабличным способом, вдруг места мало будет)
*                :
* Input          : pcBlock -указатель на данные
*                : len     -длина данных
* Return         : расчитанна€ контрольна€ сумма
*******************************************************************************/
uint16_t Crc16( void *pcBlock, uint32_t len)
{
    uint8_t   *pch;
    uint16_t  crc;

    pch = (uint8_t*)pcBlock;  crc = 0xFFFF;

    while(len--) crc = (crc<<8)^Crc16Table[(crc>>8)^(*pch++)];

    return crc;
}


/*****************************************************************************************
* Function Name  : AddToCrc16
* Description    : ѕроизводит перерасчет CRC16 при добавлении нового байта в последова-
*                : тельность
*                :
* Input          : crc  - текущее значение CRC16
*                : val  - очередной байт последовательности
*                :
* Return         : перерасчитанна€ контрольна€ сумма
*****************************************************************************************/
uint16_t AddToCrc16( uint16_t crc, uint8_t val )
{
    return (crc<<8)^Crc16Table[(crc>>8)^val];
}

// ƒобавить CRC16 буфера данных к накопленной CRC16
uint16_t AddBufToCrc16(uint16_t crc, void *pcBlock, uint32_t len)
{
    uint8_t   *pch;
    pch = (uint8_t*)pcBlock;
    while(len--) crc = (crc<<8)^Crc16Table[(crc>>8)^(*pch++)];
    return crc;
}


